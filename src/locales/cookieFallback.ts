export function getCookieFallback(languageCode: string) {
  const code = languageCode.split('-')[0].toLowerCase();
  const map = { fr, en, es, pt, ar, hi, zh, id, ru, sw, de: en, it: en, nl: en } as const;
  const localized = map[code as keyof typeof map] ?? en;
  return { ...en, ...localized };
}
