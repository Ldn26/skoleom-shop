export function getA11yFallback(languageCode: string) {
  const code = languageCode.split('-')[0].toLowerCase();
  const map = { en, fr, es, pt, ru, sw, zh, hi, id, ar, de: en, it: en, nl: en } as const;
  return map[code as keyof typeof map] ?? en;
}
