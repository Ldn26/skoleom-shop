function localizeDeep(value, lang) {
  if (typeof value === 'string') return pickString(value, lang);
  if (Array.isArray(value)) return value.map((item) => localizeDeep(item, lang));
  if (value && typeof value === 'object') {
    return Object.fromEntries(Object.entries(value).map(([k, v]) => [k, localizeDeep(v, lang)]));
  }
  return value;
}

export function getMegaVariants(megaVariantsFr, megaVariantsEn, lang) {
  if (lang === 'fr') return megaVariantsFr;
  if (lang === 'en') return megaVariantsEn;
  return localizeDeep(megaVariantsEn, lang);
}

export function getMegaLinks(megaLinksFr, megaLinksEn, lang) {
  if (lang === 'fr') return megaLinksFr;
  const out = { ...megaLinksEn };
  for (const [key, frLabel] of Object.entries(megaLinksFr)) {
    if (megaLinksEn[key] === frLabel || key in out) {
      out[key] = pickString(frLabel, lang);
    }
  }
  return out;
}

export function getNavbarProsLinks(NAVBAR_PROS_LINKS_EN, labels, lang) {
  const out = {};
  for (const label of labels) {
    const k = label
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '_')
      .replace(/^_+|_+$/g, '');
    if (lang === 'fr') {
      out[k] = label;
    } else {
      out[k] = pickString(label, lang) ?? NAVBAR_PROS_LINKS_EN[label] ?? label;
    }
  }
  return out;
}

