export function pickString(source, lang) {
  if (lang === 'fr') return source;
  const frRow = FR_STRINGS[source];
  if (frRow) return frRow[lang] ?? frRow.de ?? frRow.it ?? frRow.nl ?? frRow.en ?? source;
  const enRow = EN_STRINGS[source];
  if (enRow) return enRow[lang] ?? enRow.en ?? source;
  return source;
}

