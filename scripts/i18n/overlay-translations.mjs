export const OVERLAY_LANGS = [
  'fr',
  'en',
  'es',
  'pt',
  'ar',
  'hi',
  'zh',
  'id',
  'ru',
  'sw',
  'de',
  'it',
  'nl',
];

export function overlayLang(lang) {
  return OVERLAY_LANGS.includes(lang) ? lang : 'en';
}

/** @param {string} lang @param {Record<string, string>} map */
export function L(lang, map) {
  return map[overlayLang(lang)] ?? map.en;
}

